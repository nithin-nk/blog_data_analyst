    @staticmethod
    def _process_mermaid(html: str) -> str:
        """Process Mermaid diagram code blocks."""
        soup = BeautifulSoup(html, 'html.parser')
        
        # Find code blocks with 'mermaid' language
        for code_block in soup.find_all('code'):
            if 'mermaid' in code_block.get('class', []):
                # Convert code block to mermaid div
                mermaid_div = soup.new_tag('div', **{'class': 'mermaid'})
                mermaid_div.string = code_block.get_text()
                
                # Replace pre > code with just the div
                parent = code_block.parent
                if parent and parent.name == 'pre':
                    parent.replace_with(mermaid_div)
        
        return str(soup)
