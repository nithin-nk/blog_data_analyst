"""
Markdown to HTML converter.

Converts markdown blog posts to styled HTML with:
- Syntax highlighting for code
- Mermaid diagram rendering
- Responsive design
- Embedded images
"""

from pathlib import Path
from typing import Optional
import markdown
from markdown.extensions import fenced_code, tables, toc, codehilite
from bs4 import BeautifulSoup

from src.utils.logger import setup_logger


logger = setup_logger(__name__)


class MarkdownToHTMLConverter:
    """Converter from markdown to styled HTML."""
    
    def __init__(self) -> None:
        """Initialize the converter."""
        self.md = markdown.Markdown(
            extensions=[
                'fenced_code',
                'tables',
                'toc',
                'codehilite',
                'nl2br',
                'sane_lists',
            ],
            extension_configs={
                'codehilite': {
                    'css_class': 'highlight',
                    'linenums': False,
                },
            },
        )
        logger.info("MarkdownToHTMLConverter initialized")
    
    def convert(
        self,
        markdown_content: str,
        title: str,
        meta_description: str,
        image_path: Optional[Path] = None,
    ) -> str:
        """
        Convert markdown to full HTML page.
        
        Args:
            markdown_content: Blog content in markdown
            title: Page title
            meta_description: SEO meta description
            image_path: Optional header image path
            
        Returns:
            Complete HTML document
        """
        logger.info("Converting markdown to HTML")
        
        # Convert markdown to HTML
        body_html = self.md.convert(markdown_content)
        
        # Wrap in full HTML document
        html = self._create_html_document(
            body_html,
            title,
            meta_description,
            image_path,
        )
        
        # Process Mermaid diagrams
        html = self._process_mermaid(html)
        
        logger.info("Conversion complete")
        return html
    
    def _create_html_document(
        self,
        body_html: str,
        title: str,
        meta_description: str,
        image_path: Optional[Path],
    ) -> str:
        """Create complete HTML document with styling."""
        
        image_tag = ""
        if image_path:
            image_tag = f'<img src="{image_path}" alt="{title}" class="header-image">'
        
        html = f"""<!DOCTYPE html>
<html lang="en">
<head>
    <meta charset="UTF-8">
    <meta name="viewport" content="width=device-width, initial-scale=1.0">
    <meta name="description" content="{meta_description}">
    <title>{title}</title>
    
    <!-- Mermaid JS for diagrams -->
    <script type="module">
        import mermaid from 'https://cdn.jsdelivr.net/npm/mermaid@10/dist/mermaid.esm.min.mjs';
        mermaid.initialize({{ startOnLoad: true }});
    </script>
    
    <!-- Syntax Highlighting -->
    <link rel="stylesheet" href="https://cdnjs.cloudflare.com/ajax/libs/highlight.js/11.9.0/styles/github-dark.min.css">
    
    <style>
        {self._get_css()}
    </style>
</head>
<body>
    <article class="blog-post">
        {image_tag}
        <header>
            <h1>{title}</h1>
        </header>
        <main>
            {body_html}
        </main>
        <footer>
            <p>Generated by Blog Data Analyst AI Agent</p>
        </footer>
    </article>
</body>
</html>"""
        
        return html
    
